package Models;

public class PlanosModel{
	
	public float ValorPlano;
	
	public float getValorPlano() {
		return ValorPlano;
	}
	
	public void setValorPlano(float valorPlano) {
		this.ValorPlano = valorPlano;
	}
	
	public int CodigoPlano;
	
	public int getCodigoPlano() {
		return CodigoPlano;
	}
	
	public void setCodigoPlano(int codigoPlano) {
		this.CodigoPlano = codigoPlano;
	}
	
	public String NomePlano; 
	
	public String getNomePlano() {
		return NomePlano;
	}
	
	public void setNomePlano(String nomePlano) {
		this.NomePlano = nomePlano;
	}

}
